#include <stdio.h>

/**
 * add - addition
 * @a: ...
 * @b: ...
 * Return: a result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtration
 * @a: ...
 * @b: ...
 * Return: a result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplication
 * @a: ...
 * @b: ...
 * Return: a result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division
 * @a: ...
 * @b: ...
 * Return: a result
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modular
 * @a: ...
 * @b: ...
 * Return: a result
 */

int mod(int a, int b)
{
	return (a % b);
}
